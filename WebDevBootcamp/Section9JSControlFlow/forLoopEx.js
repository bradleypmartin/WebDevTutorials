// for loop exercises. note how close to C++ the syntax is.

console.log("Ex 1: print all nums between -10 and 19.");
for(var i=-10; i<=19; i++){
  console.log(i);
}

console.log("Ex 2: print all even nums between 10 and 40.");
for(var i=10; i<=40; i+=2){
  console.log(i);
}

console.log("Ex 3: print all odd nums between 300 and 333.");
for(var i=301; i<=333; i+=2){
  console.log(i);
}

console.log("Ex 4: print all nums div by 5 AND 3 between 5 and 50.");
for(var i=15; i<=50; i+=15){
  console.log(i);
}
